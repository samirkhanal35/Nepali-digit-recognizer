def unicode(c):

    a = len(c)
    for i in range(0,a):
        if c[i] == 0 : return (str(u'\u0966'))
        elif c[i] == 1 : return (str(u'\u0967'))
        elif c[i] == 2 : return (str(u'\u0968'))
        elif c[i] == 3 : return (str(u'\u0969'))
        elif c[i] == 4 : return (str(u'\u096A'))
        elif c[i] == 5 : return (str(u'\u096B'))
        elif c[i] == 6 : return (str(u'\u096C'))
        elif c[i] == 7 : return (str(u'\u096D'))
        elif c[i] == 8 : return (str(u'\u096E'))
        else : return (str(u'\u096F'))


